'''Listas: Coleções ordenadas e mutáveis que permitem elementos duplicados. 
Ótimas para armazenar sequências homogêneas ou heterogêneas.'''

minha_lista = [1, "python", 2, 3]

'''Tuplas (tuple): Coleções ordenadas e imutáveis. 
Úteis para garantir que os dados não sejam alterados.'''

minha_tupla = (1, 2, 3)

'''Dicionários (dict): Conjuntos não ordenados de pares chave-valor. 
As chaves são únicas e imutáveis.'''

meu_dict = {"nome": "Ana", "idade": 25}

'''Conjuntos (set): Coleções não ordenadas de elementos únicos. 
Ideais para eliminar duplicatas e realizar operações matemáticas de conjuntos.'''

meu_set = {1, 2, 3, 3}

